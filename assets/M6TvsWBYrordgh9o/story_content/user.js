window.Script319 = function()
{
  function updateGlassEffect() {
  const squares = document.querySelectorAll('[data-acc-text*="semi-transparent"]');
  if (!squares.length) {
    console.log("No square shapes found!");
    return;
  }
  squares.forEach(square => {
    square.style.backdropFilter = "blur(10px) contrast(0.75)";
    square.style.borderRadius = "0";
  });
}

updateGlassEffect();
window.addEventListener("resize", updateGlassEffect);
}

